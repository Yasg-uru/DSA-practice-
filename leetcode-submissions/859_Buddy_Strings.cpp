class Solution {
public:
    bool buddyStrings(string s, string goal) {
        for(int i=0;i<s.length();i++){
            for(int j=i+1;j<s.length();j++){
                swap(s[i],s[j]);
                if(s==goal){
                    return true ;
                }
                swap(s[i],s[j]);
            }
        }
        return false ;
    }
};